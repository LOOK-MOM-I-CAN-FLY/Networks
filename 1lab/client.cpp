// iostream: Нужна для ввода и вывода текста в консоль (std::cin и std::cout).
#include <iostream>

// string: Подключает класс строк (std::string), чтобы удобно работать с текстом.
#include <string>

// cstring: Старая Си-библиотека для работы с массивами символов. 
// В этом коде она фактически не нужна, но часто идет "прицепом" в сетевых программах.
#include <cstring>

// arpa/inet.h: Содержит функции для перевода IP-адресов из понятного нам вида ("127.0.0.1") 
// в понятный компьютеру (набор нулей и единиц). Например: inet_pton, htons.
#include <arpa/inet.h>

// sys/socket.h: Самая главная сетевая библиотека. Содержит базовые функции сокетов: 
// socket() - создать, sendto() - отправить, recvfrom() - получить.
#include <sys/socket.h>

// netinet/in.h: Содержит структуры данных для интернет-адресов (например, sockaddr_in), 
// а также константы вроде AF_INET (IPv4).
#include <netinet/in.h>

// unistd.h: Библиотека операционной системы (POSIX). Здесь она нужна для функции close(), 
// чтобы правильно закрыть сокет в конце.
#include <unistd.h>

// cstdlib: Стандартная библиотека языка Си. Нужна здесь ради одной функции: atoi(), 
// которая превращает текст "123" в число 123.
#include <cstdlib>
// Это память, выделенная операционной системой при запуске программы.
int main(int argc,char**argv){

    // const char* - это указатель на неизменяемую строку. Мы говорим: "ip будет указывать на текст".
    // Знак вопроса '?' и двоеточие ':' — это тернарный оператор (сокращенный if-else).
    // Читается так: "Если аргументов больше 1 (argc > 1), ТО возьми первый аргумент (argv[1]), 
    // ИНАЧЕ возьми строку "127.0.0.1" (это IP-адрес твоего же компьютера, localhost)".
    const char* ip = argc > 1 ? argv[1] : "127.0.0.1";

    // Порт — это "номер двери" в компьютере, куда мы стучимся.
    // atoi(argv[2]) - функция "ASCII to Integer". Она берет текст (например, "8080") 
    // и делает из него математическое число 8080. 
    // 12345 - это порт по умолчанию, если пользователь ничего не ввел при запуске.
    int port = argc > 2 ? atoi(argv[2]) : 12345;

    // Функция socket() просит у операционной системы создать "сетевую розетку".
    // AF_INET - это константа. Означает, что мы используем адреса версии IPv4 (вида 192.168.0.1).
    // SOCK_DGRAM - означает Datagram. Это говорит системе, что мы используем протокол UDP.
    // 0 - выбор протокола по умолчанию (для SOCK_DGRAM это всегда UDP).
    // Возвращает число (int). Это число называется "файловый дескриптор". 
    // Это просто номерок (например, 3), по которому ОС понимает, с каким сокетом мы работаем.
    int s = socket(AF_INET, SOCK_DGRAM, 0);
    
    // sockaddr_in - структура (набор переменных), которая хранит IP-адрес и порт.
    // srv{} - фигурные скобки инициализируют всю структуру нулями. 
    // Это важно для памяти! Иначе там будет "мусор" от старых программ, и сеть может не сработать.
    sockaddr_in srv{}; 
    
    // sin_family: указываем семейство адресов. Снова AF_INET (IPv4).
    srv.sin_family = AF_INET; 
    
    // sin_port: указываем порт. 
    // htons() расшифровывается как Host TO Network Short. 
    // Разные процессоры читают байты по-разному (слева-направо или справа-налево). 
    // Сеть требует единого порядка. htons() переворачивает байты числа в нужный сетевой стандарт.
    srv.sin_port = htons(port); 
    
    // inet_pton() - расшифровывается как Presentation TO Network. 
    // Превращает красивый текст ("127.0.0.1") во внутренний бинарный формат компьютера 
    // и кладет этот результат в переменную srv.sin_addr.
    // Знак '&' (амперсанд) - это передача адреса памяти. Мы говорим функции: "положи результат вот сюда".
    inet_pton(AF_INET, ip, &srv.sin_addr);
    // Создаем умную строку C++. Она сама управляет своей памятью (выделяет ее в Куче - Heap).
    std::string line;

    // std::getline(std::cin, line) - ждет, пока ты введешь текст в консоль (std::cin) и нажмешь Enter. 
    // Как только нажал, текст кладется в переменную line. 
    // Цикл while работает бесконечно, пока ты не закроешь программу (или не нажмешь Ctrl+D).
    while(std::getline(std::cin, line)) {

        // --- ОТПРАВКА ДАННЫХ ---
        // sendto() - функция отправки UDP-пакета.
        // s - наш номер сокета.
        // line.data() - берем сырой текст из нашей строки.
        // line.size() - сколько букв (байт) мы отправляем.
        // 0 - флаги. Нам никакие спец. настройки тут не нужны.
        // (sockaddr*)&srv - Очень важный момент для препода! Это приведение типов (каст). 
        // Функция sendto старая и требует общую структуру sockaddr, а мы используем современную sockaddr_in. 
        // Мы берем адрес нашей структуры (&srv) и "убеждаем" компилятор смотреть на нее как на (sockaddr*).
        // sizeof(srv) - размер нашей структуры в байтах, чтобы ОС знала, сколько памяти читать.
        sendto(s, line.data(), line.size(), 0, (sockaddr*)&srv, sizeof(srv));

        // --- ПОДГОТОВКА К ПРИЕМУ ---
        // char b[1024]; - создаем массив из 1024 символов. 
        // Память для него выделяется на СТЕКЕ (Stack). Это быстрая, но ограниченная память. 
        // Это наш "буфер" - коробка, куда мы положим ответ от сервера.
        char b[1024];

        // socklen_t - специальный тип данных для хранения длины адреса.
        // Нам нужно передать размер структуры, чтобы функция recvfrom знала, сколько места у нее есть.
        socklen_t sl = sizeof(srv);

        // --- ПОЛУЧЕНИЕ ДАННЫХ ---
        // recvfrom() - функция получения ответа. Программа тут "зависнет" и будет ждать ответа.
        // s - слушаем наш сокет.
        // b - куда складывать ответ (в наш массив b).
        // sizeof(b)-1 - максимальное количество байт для чтения. Мы пишем -1 (то есть 1023), 
        // чтобы оставить одно место под нулевой символ (конец строки).
        // 0 - флаги (нет).
        // (sockaddr*)&srv - сюда запишется адрес того, кто нам ответил (сервера).
        // &sl - указатель на переменную с размером адреса.
        // ssize_t n - сохраняем количество реально полученных байт. ssize_t означает "со знаком",
        // то есть n может быть -1 в случае ошибки.
        ssize_t n = recvfrom(s, b, sizeof(b)-1, 0, (sockaddr*)&srv, &sl);

        // Если n > 0 (мы получили хоть 1 байт ответа и не было ошибок):
        if(n > 0) { 
            // b[n] = 0; - Очень важно для памяти! В языке Си строки должны заканчиваться невидимым символом нуля '\0'.
            // Иначе std::cout пойдет читать мусор из памяти дальше по массиву и выведет кракозябры.
            b[n] = 0; 
            
            // Выводим полученный ответ в консоль и делаем перенос строки ("\n").
            std::cout << b << "\n"; 
        }
    }

    // Когда цикл закончится, закрываем наш сокет "s". 
    // Это как положить трубку телефона, чтобы операционная система освободила память и ресурсы.
    close(s);
}
